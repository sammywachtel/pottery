    USING (bucket_id = 'pottery-photos' AND auth.uid()::text = (storage.foldername(name))[1]);

CREATE POLICY "Users can delete their own photos" ON storage.objects
    FOR DELETE TO authenticated
    USING (bucket_id = 'pottery-photos' AND auth.uid()::text = (storage.foldername(name))[1]);
